public class WhileLoop {
  public static void main(String[] args) {
    // # While Loop:
    //  - While loop is a simpler version of loop than for loop.
    //  - In the while loop, there are only loop conditions, without init statements and post statements.
    //
    // # Example:

    var c = 1;
    while (c <= 10) {
      System.out.println("Loop Number - "+ c);
      c++;
    }
  }
}
