public class TypeDataString {
  public static void main(String[] args) {
    // # Type Data String
    //
    //   - Type data that has values of character (text).
    //   - In Java, type data String represented by type String.
    //   - To create a String in Java, we are using a character double quotation marks (""), at the beginning and at the end text.
    //   - Default value of String is null.
    //
    // # Example:

    String firstName = "John";
    String lastName = "Doe";

    System.out.println(firstName);
    System.out.println(lastName);

    // # Combining String Values
    //
    //  - using plus char (+)
    //
    // Example:

    String fullName = firstName + " " + lastName;
    System.out.println(fullName);
  }
}
