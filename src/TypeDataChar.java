public class TypeDataChar {
  public static void main(String[] args) {
    // # Type Data Character
    //
    //   - A type data character (letter) in java represented by type char
    //   - To make type data char in java, we can use a single quotation mark ('') at the beginning and at the end character.
    //
    // # Example:

    char y = 'Y';
    char o = 'O';
    char u = 'U';

    System.out.print(y);
    System.out.print(o);
    System.out.print(u);
  }
}
